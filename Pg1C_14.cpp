/*/////////////////////////////////////////////////////////////////////////////////////
課題C14. for文を使って、○を縦横に10個ずつ四角形に並べるプログラムを作成しなさい。○は一つずつ出力すること。

キーワード：for文、二重の繰り返し

最終更新日：2015.04.01
*/
#define _CRT_SECURE_NO_WARNINGS  // 余計な警告が出ないようにする
#include	<stdio.h>

int	main( void )
{
	int	i, j;
	for(i=0;i<10;i++){
		for(j=0;j<10;j++){
			printf("○");
		}
		printf("\n");
	}
	return 0;
}
